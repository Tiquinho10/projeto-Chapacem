    public void readTable() {
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();

        modelo.setNumRows(0);

        for (Passageiro p : PassageiroController.listaPassageiro()) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                p.getApelido(),
                p.getContacto(),
                p.getNomeUsuario()
            });
        }

    }

    public void readTableByUsuario() {
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();

        modelo.setNumRows(0);

        List<Passageiro> lista = PassageiroController.listaPorNomeUsuario(pesquisaIn.getText());

        if (lista.size() == 0) {
            JOptionPane.showMessageDialog(this, "Usuario " + pesquisaIn.getText() + " nao encontrado");

            readTable();

            return;
        }

        for (Passageiro p : lista) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                p.getApelido(),
                p.getContacto(),
                p.getNomeUsuario()
            });
        }

    }

