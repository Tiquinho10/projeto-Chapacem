    private void atualizarPrecoBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizarPrecoBtn1ActionPerformed
        // TODO add your handling code here:

        if (d1Valor.getText().length() == 0 || d2Valor.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Por favor, preencha todos campos");

            return;

        }

        float d1 = Float.parseFloat(d1Valor.getText());
        float d2 = Float.parseFloat(d2Valor.getText());

        if (BilheteController.atualizarPrecario(d1, d2)) {
            d1Valor.setEditable(false);
            d2Valor.setEditable(false);

            atualizarPrecoBtn1.setEnabled(false);
            atualizarPrecoBtn1.setVisible(false);
            cancelarBtn.setEnabled(false);
            cancelarBtn.setVisible(false);

            atualizarPrecoBtn.setEnabled(true);
            atualizarPrecoBtn.setVisible(true);
        }
    }//GEN-LAST:event_atualizarPrecoBtn1ActionPerformed

